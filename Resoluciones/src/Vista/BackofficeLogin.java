    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        try{ 
            String id = txtUsuario.getText();
            String pass = txtContrasenha.getText();
            System.out.println(id + " " + pass);
            estrategiaLogin = new LoginMySQL();
            DTOUsuario usuario = estrategiaLogin.Login(id, pass);
            JFrame frame;
            if("Coordinador".equals(usuario.getTipoUsuario()))
                 frame = new BackofficeCoordinador();
            else frame = new BackofficeEstudiante();
            this.setVisible(false);
            frame.setVisible(true);
        }
        catch(Exception e){
            showMessage(e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

